    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        quantidade = Double.parseDouble(txtQuantidade.getText());
       
        if (cbCombustivel.getSelectedIndex() == 0) {
            total = quantidade * precoGasolina;
            txtTotal.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(total)));
            
            totalImpostos = (total/100)*48.9;
            txtImpostos.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(totalImpostos)));
        }
        
        if (cbCombustivel.getSelectedIndex() == 1) {
            total = quantidade * precoAlcool;
            txtTotal.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(total)));
            
            totalImpostos = (total/100)*48.9;
            txtImpostos.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(totalImpostos)));
        }
        
        if (cbCombustivel.getSelectedIndex() == 2) {
            total = quantidade * precoDiesel;
            txtTotal.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(total)));
            
            totalImpostos = (total/100)*22.8;
            txtImpostos.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(totalImpostos)));
        }
        
    }//GEN-LAST:event_btnCalcularActionPerformed

